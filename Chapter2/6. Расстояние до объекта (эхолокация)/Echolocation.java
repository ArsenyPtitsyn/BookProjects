/*
	Программа определяет расстояние до крупного объекта по времени, 
	в течение которого звук от хлопка рекошетит от этого объекта (7.2 c)
*/
class Echolocation{
	public static void main(String args[]){
		double dist;

		dist = 1100 * 7.2 / 2;

		System.out.println("Расстояние до крупного удалённого объекта составляет " + dist + " футов");
	}
}