/*
	Демонстрация управляющих последовательностей в символьных строках
*/
class StrDemo{
	public static void main(String args[]){
		System.out.println("Первая строка\nВторая строка"); // используется символ перевода строки.
		System.out.println("A\tB\tC"); // используется табуяция для выравнивания вывода
		System.out.println("D\tE\tF"); // используется табуяция для выравнивания вывода
	}
}